def test_execute(small_sample: list[str]) -> None:
    register_states = execute(small_sample)

    assert len(register_states) == 6
    assert register_states[0] == 1  # Start state
    assert register_states[1] == 1  # Noop runs
    assert register_states[2] == 1  # Noop runs
    assert register_states[3] == 1  # addX 3 runs
    assert register_states[4] == 1  # addX 3 runs
    assert register_states[5] == 4  # addX -5 runs
    assert register_states[6] == 4  # addX -5 runs


def test_execute(large_sample: list[str]) -> None:
    register_states = execute(large_sample)

    assert register_states[20] == 21  # During 20th cycle
    assert register_states[60] == 19  # During 60th cycle
    assert register_states[100] == 18  # During 100th cycle
    assert register_states[140] == 21  # During 140th cycle
    assert register_states[180] == 16  # During 180th cycle
    assert register_states[220] == 18  # During 220th cycle


